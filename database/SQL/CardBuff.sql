DROP TABLE IF EXISTS `CardBuff`;
CREATE TABLE `CardBuff` (
`ID` int(11) AUTO_INCREMENT NOT NULL ,
`CardTypeID` int(11) NOT NULL DEFAULT '0',
`StartTime` datetime(0) NOT NULL DEFAULT '0000-00-00 00:00:00',
`EndTime` datetime(0) NOT NULL DEFAULT '0000-00-00 00:00:00',
PRIMARY KEY (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;